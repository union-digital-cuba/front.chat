const Console = {
  Info: (message) => {
    // eslint-disable-next-line no-console
    console.info(message)
  },
  Log: (message) => {
    // eslint-disable-next-line no-console
    console.log(message)
  },
  Error: (message) => {
    // eslint-disable-next-line no-console
    console.error(message)
  },
}

export default Console
